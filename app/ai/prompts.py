QUESTION_FALLBACK = [
    "Что важно подчеркнуть в этом посте?",
    "Есть ли фото или видео для публикации?",
    "Нужен ли мягкий призыв к действию?",
]


def _task_block(task: ContentTask) -> str:
    lines = [
        f"Рубрика: {task.rubric or '—'}",
        f"Тема: {task.topic or '—'}",
        f"Цель поста: {task.goal or '—'}",
    ]
    if task.description:
        lines.append(f"Описание задачи: {task.description}")
    return "\n".join(lines)


def _answers_block(task: ContentTask) -> str:
    if not task.answers:
        return "Владелец не дал дополнительных ответов."
    return "Ответы владельца:\n" + "\n".join(f"— {a.answer_text}" for a in task.answers)


def _media_block(task: ContentTask) -> str:
    if not task.media:
        return "Медиа не прикреплено, пост будет текстовым."
    photos = sum(1 for m in task.media if m.media_type == "photo")
    videos = sum(1 for m in task.media if m.media_type == "video")
    parts = []
    if photos:
        parts.append(f"фото: {photos}")
    if videos:
        parts.append(f"видео: {videos}")
    return "К посту прикреплено медиа (" + ", ".join(parts) + "). Учитывай, что текст пойдёт подписью к нему."


def build_questions_prompt(task: ContentTask) -> str:
    return (
        "Задача из календаря контента:\n"
        f"{_task_block(task)}\n\n"
        "Сформулируй от 1 до 3 коротких уточняющих вопросов владельцу канала, "
        "ответы на которые помогут написать хороший пост. Не перегружай владельца: "
        "если задача уже понятна, достаточно одного вопроса.\n"
        "Верни только вопросы, каждый с новой строки, без нумерации и пояснений."
    )


# Метки, между которыми модель обязана вернуть ЧИСТЫЙ текст поста — без вступлений
# вроде «Вот черновик поста:» или рассуждений до/после. Это не публикуется, только
# используется для извлечения (см. content_tasks.extract_marked) — защита от того,
# что комментарии модели случайно попадут в текст, отправляемый в канал/на согласование.
POST_START = "===ПОСТ==="
POST_END = "===КОНЕЦ==="

_MARKER_INSTRUCTION = (
    f"\n\nОБЯЗАТЕЛЬНО верни результат строго между метками {POST_START} и {POST_END}, "
    "без единого слова до или после меток — ни вступления, ни пояснений, ни рассуждений. "
    "Внутри меток — только сам текст поста, готовый к публикации."
)


def build_generation_prompt(task: ContentTask) -> str:
    return (
        "Напиши черновик поста для Telegram-канала.\n\n"
        f"{_task_block(task)}\n\n"
        f"{_answers_block(task)}\n\n"
        f"{_media_block(task)}"
        f"{_MARKER_INSTRUCTION}"
    )


def build_revision_prompt(task: ContentTask, previous_text: str, revision_comment: str) -> str:
    return (
        "Ниже черновик поста и правки владельца. Перепиши черновик с учётом правок, "
        "сохранив задачу и стиль.\n\n"
        f"{_task_block(task)}\n\n"
        f"Текущий черновик:\n{previous_text}\n\n"
        f"Правки владельца:\n{revision_comment}"
        f"{_MARKER_INSTRUCTION}"
    )


def build_alternative_prompt(task: ContentTask, previous_text: str) -> str:
    return (
        "Владельцу не подошёл вариант ниже. Напиши другой вариант поста на ту же задачу: "
        "другой заход, другая подача, но та же тема и цель.\n\n"
        f"{_task_block(task)}\n\n"
        f"{_answers_block(task)}\n\n"
        f"Предыдущий вариант (не повторяй его структуру):\n{previous_text}"
        f"{_MARKER_INSTRUCTION}"
    )
